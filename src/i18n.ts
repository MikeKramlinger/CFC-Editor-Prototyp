export const getStoredLanguage = (): UiLanguage | null => {
  const stored = localStorage.getItem("cfc-editor-language");
  return stored === "de" || stored === "en" ? stored : null;
};

export const storeLanguage = (lang: UiLanguage): void => {
  localStorage.setItem("cfc-editor-language", lang);
};

